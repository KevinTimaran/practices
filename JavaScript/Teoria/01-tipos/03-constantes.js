let name = 'John Doe'; // Let permite reasignar el valor de la variable en el futuro
name = 'Jane Doe'; // Esto es válido, ya que 'name' es una variable

const nombre = 'John Doe'; // Constante, no se puede reasignar
// nombre = 'Jane Doe'; // Esto causará un error, ya que 'nombre' es una constante

console.log(name); // Imprime: Jane Doe
console.log(nombre); // Imprime: John Doe