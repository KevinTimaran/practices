package Teoria;

public class operadores_Comparacion {
    public static void main(String[] args) {
        // Allows comparing two datas and delivery false or true
        int a = 15;
        int b = 10;

        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a <= b);
        System.out.println(a >= b);

    }
}
