package Teoria;

public class operadores_Logicos {
    public static void main(String[] args) {


        boolean a = true;
        boolean b = false;

        // Function or
        boolean c = a || b; //This function search a True

        // Function and
        boolean d = a && b; // This function wait that appears the firsts value of false  and assign false to b


        // Function not
        boolean e = !a; // invest is value


        System.out.println(c);
        System.out.println(d);
        System.out.println(e);












    }
}
