package Teoria;

public class operadores_aritmeticos {
    public static void main(String[] args) {

        int a = 2+2;
        int b = 3-2;
        int c = 4*3;
        int m = 2 % 5; //Indica la parte restante de la division
        float d = 10f / 3f; // If i want how solution a number float is necessary put one f in the numbers
        double e = 10.0 / 3.0;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(m);
        System.out.println(e);


        // This is the way to increase a number to one variable
        int x = 5;
        System.out.println(x);
        ++x;
        System.out.println(x);

        int r = 10;
        int t = ++r;
        System.out.println(r);
        System.out.println(t);

        //And this is the way to increase more to one data to the variable
        int q = 5;
        q +=10;
        System.out.println(q);




    }
}
